def calculate_beam_f3_coordinates(mesh, m2m_dir, x_f3, y_f3):
    """Calculate Beam F3 coordinates using the provided X_F3 and Y_F3 distances.
    
    Args:
        mesh: PyVista mesh object
        m2m_dir: Directory containing EEG positions
        x_f3: X_F3 distance from T7 in mm
        y_f3: Y_F3 distance from Cz in mm
        
    Returns:
        tuple: (beam_f3_coords, ref_beam_f3_coords, paths_dict) containing the coordinates
              of Beam F3 and Ref Beam F3, and dictionary of paths for visualization
    """
    # Create measurements directory if it doesn't exist
    measurements_dir = os.path.join(m2m_dir, "measurements")
    os.makedirs(measurements_dir, exist_ok=True)
    
    # Get coordinates from EEG positions CSV
    eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
    eeg_df = pd.read_csv(eeg_path, header=None)
    label_col = eeg_df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    
    # Get required electrode coordinates
    electrodes = ["Nz", "Fpz", "T7"]
    electrode_coords = {}
    electrode_indices = {}
    
    # Get F3 coordinates first to include in the returned paths_dict
    f3_match = eeg_df[eeg_df[label_col] == "F3"]
    original_f3_coords = None
    if not f3_match.empty:
        original_f3_coords = f3_match.iloc[0, coord_cols].astype(float).values
    
    for electrode in electrodes:
        match = eeg_df[eeg_df[label_col] == electrode]
        if match.empty:
            raise ValueError(f"{electrode} not found in EEG positions CSV")
        coords = match.iloc[0, coord_cols].astype(float).values
        electrode_coords[electrode] = coords
    
    # Prepare mesh for geodesic calculations
    points, faces = prepare_mesh_for_geodesic(mesh)
    geoalg = PyGeodesicAlgorithmExact(points, faces)
    
    # Find Vertex_Real directly
    vertex_Real, _ = find_vertex_Real(mesh, m2m_dir, geoalg)
    print("Using Vertex_Real for Beam F3 calculation")
    electrode_coords["Vertex_Real"] = vertex_Real
    
    # Find indices for all points in the geodesic mesh
    for point_name, coords in electrode_coords.items():
        idx = np.argmin(np.linalg.norm(points - coords, axis=1))
        electrode_indices[point_name] = idx
    
    # First find EB point (1/3 along Nz-Fpz path)
    distance_nz_fpz, path_nz_fpz = geoalg.geodesicDistance(
        electrode_indices["Nz"], 
        electrode_indices["Fpz"]
    )
    path_points_nz_fpz = np.array(path_nz_fpz)
    
    # Calculate EB position
    total_distance = 0
    path_distances = []
    for i in range(1, len(path_points_nz_fpz)):
        segment_distance = np.linalg.norm(path_points_nz_fpz[i] - path_points_nz_fpz[i-1])
        total_distance += segment_distance
        path_distances.append(total_distance)
    
    target_distance = total_distance * 1/3
    eb_idx = np.argmin(np.abs(np.array(path_distances) - target_distance))
    eb_point = path_points_nz_fpz[eb_idx + 1]
    eb_idx_geo = np.argmin(np.linalg.norm(points - eb_point, axis=1))
    
    # Calculate path from EB to T7
    distance_eb_t7, path_eb_t7 = geoalg.geodesicDistance(
        eb_idx_geo,
        electrode_indices["T7"]
    )
    path_points_eb_t7 = np.array(path_eb_t7)
    
    # Find Off_Mid_F3 point by measuring X_F3 from EB towards T7
    reversed_path = path_points_eb_t7[::-1]  
    
    total_distance = 0
    path_distances = []
    # Start measuring from EB (beginning of reversed path)
    for i in range(1, len(reversed_path)):
        segment_distance = np.linalg.norm(reversed_path[i] - reversed_path[i-1])
        total_distance += segment_distance
        path_distances.append(total_distance)
        if total_distance >= x_f3:
            off_mid_f3_idx = i
            break
    else:
        raise ValueError(f"X_F3 distance {x_f3} is longer than T7-EB path length {total_distance}")
    
    # Get the point X_F3 distance from EB
    off_mid_f3_point = reversed_path[off_mid_f3_idx]
    off_mid_f3_idx_geo = np.argmin(np.linalg.norm(points - off_mid_f3_point, axis=1))
    
    # Calculate path from Vertex_Real to Off_Mid_F3
    distance_vertex_real_to_off_mid_f3, path_vertex_real_to_off_mid_f3 = geoalg.geodesicDistance(
        electrode_indices["Vertex_Real"],
        off_mid_f3_idx_geo
    )
    path_points_vertex_real_to_off_mid_f3 = np.array(path_vertex_real_to_off_mid_f3)
    
    # Find Beam_F3 point (Y_F3 distance starting from Off_Mid_F3 towards Vertex_Real)
    total_distance = 0
    path_distances = []
    # Use the path from Off_Mid_F3 to Vertex_Real
    for i in range(1, len(path_points_vertex_real_to_off_mid_f3)):
        segment_distance = np.linalg.norm(path_points_vertex_real_to_off_mid_f3[-i] - path_points_vertex_real_to_off_mid_f3[-(i+1)])
        total_distance += segment_distance
        path_distances.append(total_distance)
        if total_distance >= y_f3:
            beam_f3_idx = i
            break
    else:
        raise ValueError(f"Y_F3 distance {y_f3} is longer than Vertex_Real-Off_Mid_F3 path length {total_distance}")
    
    # Get the point Y_F3 distance from Off_Mid_F3
    beam_f3_point = path_points_vertex_real_to_off_mid_f3[-(beam_f3_idx + 1)]
    
    # Now find the reference point
    ref_beam_f3_point, ref_paths_dict = find_ref_beam_f3(
        mesh,
        beam_f3_point,
        m2m_dir,
        geoalg=geoalg
    )
    
    # Combine all paths for visualization
    paths_dict = {
        "EB-T7": path_points_eb_t7,
        "Vertex_Real-Off_Mid_F3": path_points_vertex_real_to_off_mid_f3,
        "EB": eb_point,
        "Off_Mid_F3": off_mid_f3_point,
        "Beam_F3": beam_f3_point,
        "Vertex_Real": vertex_Real,
        **ref_paths_dict  # Include reference paths
    }
    if original_f3_coords is not None:
        paths_dict["F3"] = original_f3_coords
    
    # Save coordinates to CSV files
    beam_f3_output = os.path.join(measurements_dir, "beam_f3_coordinates.csv")
    with open(beam_f3_output, mode="w", newline="") as file:
        writer = csv.writer(file)
        writer.writerow(["Point", "X", "Y", "Z"])
        writer.writerow(["Beam_F3"] + list(beam_f3_point))
        writer.writerow(["Ref_Beam_F3"] + list(ref_beam_f3_point))
    
    print(f"\nBeam F3 coordinates saved to {beam_f3_output}")
    print(f"Beam F3: [{beam_f3_point[0]:.2f}, {beam_f3_point[1]:.2f}, {beam_f3_point[2]:.2f}]")
    print(f"Ref Beam F3: [{ref_beam_f3_point[0]:.2f}, {ref_beam_f3_point[1]:.2f}, {ref_beam_f3_point[2]:.2f}]")
    
    return beam_f3_point, ref_beam_f3_point, paths_dict

def calculate_geodesic_angle(points, path1, path2, point_idx):
    """Calculate the angle between two geodesic paths at their intersection point.
    
    Args:
        points: Mesh points array
        path1: First geodesic path points (Nz-Cz path)
        path2: Second geodesic path points (path to Beam_F3)
        point_idx: Index in path1 where we want to calculate the angle
        
    Returns:
        float: Angle in degrees between the paths
    """
    # Find the vectors along each path
    def get_path_vector(path, idx):
        # Ensure we stay within bounds
        if idx == 0:
            return path[1] - path[0]
        elif idx >= len(path) - 1:
            return path[-1] - path[-2]
        else:
            # Use centered difference for better direction estimation
            return path[min(idx + 1, len(path)-1)] - path[max(idx - 1, 0)]
    
    # Get vector along path1 (Nz-Cz)
    v1 = get_path_vector(path1, point_idx)
    
    # Find closest point on path2 to the intersection point
    intersection_point = path1[point_idx]
    distances = np.linalg.norm(path2 - intersection_point, axis=1)
    closest_idx = np.argmin(distances)
    
    # Get vector along path2 at the closest point
    v2 = get_path_vector(path2, closest_idx)
    
    # Normalize vectors
    v1 = v1 / np.linalg.norm(v1)
    v2 = v2 / np.linalg.norm(v2)
    
    # Calculate angle between vectors
    dot_product = np.clip(np.dot(v1, v2), -1.0, 1.0)
    angle = np.arccos(dot_product)
    
    return np.degrees(angle)

def find_ref_beam_f3(mesh, beam_f3_point, m2m_dir, geoalg=None, target_angle=45.0):
    """Find the Ref_Beam_F3 point where the path to Beam_F3 intersects Nz-Vertex_Real at 45 degrees.
    
    Args:
        mesh: PyVista mesh object
        beam_f3_point: Coordinates of Beam_F3 point
        m2m_dir: Directory containing EEG positions
        geoalg: Optional pre-initialized geodesic algorithm
        target_angle: Target intersection angle in degrees (default: 45)
        
    Returns:
        tuple: (ref_point_coords, paths_dict) containing the reference point coordinates
              and dictionary of paths for visualization
    """
    # Get coordinates from EEG positions CSV
    eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
    eeg_df = pd.read_csv(eeg_path, header=None)
    label_col = eeg_df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    
    # Get Nz coordinates
    nz_match = eeg_df[eeg_df[label_col] == "Nz"]
    if nz_match.empty:
        raise ValueError("Nz not found in EEG positions CSV")
    nz_coords = nz_match.iloc[0, coord_cols].astype(float).values
    
    # Prepare mesh for geodesic calculations if needed
    if geoalg is None:
        points, faces = prepare_mesh_for_geodesic(mesh)
        geoalg = PyGeodesicAlgorithmExact(points, faces)
    else:
        points = mesh.points
    
    # Find Vertex_Real directly
    vertex_Real, _ = find_vertex_Real(mesh, m2m_dir, geoalg)
    
    # Find indices for all points
    nz_idx = np.argmin(np.linalg.norm(points - nz_coords, axis=1))
    vertex_real_idx = np.argmin(np.linalg.norm(points - vertex_Real, axis=1))
    beam_f3_idx = np.argmin(np.linalg.norm(points - beam_f3_point, axis=1))
    
    # Get Nz-Vertex_Real path
    _, path_nz_vertex = geoalg.geodesicDistance(
        nz_idx,
        vertex_real_idx
    )
    path_points_nz_vertex = np.array(path_nz_vertex)
    
    # Find the point where the intersection angle is closest to target
    best_angle_diff = float('inf')
    ref_beam_f3_point = None
    best_path_to_beam = None
    
    # Sample points along Nz-Vertex_Real path (more densely sampled)
    num_samples = min(100, len(path_points_nz_vertex))  # Increase number of samples
    sample_indices = np.linspace(0, len(path_points_nz_vertex)-1, num_samples, dtype=int)
    
    for idx in sample_indices:
        test_point = path_points_nz_vertex[idx]
        test_point_idx = np.argmin(np.linalg.norm(points - test_point, axis=1))
        
        # Calculate path from test point to Beam_F3
        _, path_to_beam = geoalg.geodesicDistance(
            test_point_idx,
            beam_f3_idx
        )
        path_points_to_beam = np.array(path_to_beam)
        
        # Calculate intersection angle
        angle = calculate_geodesic_angle(
            points,
            path_points_nz_vertex,
            path_points_to_beam,
            idx
        )
        
        # Check if this angle is closer to target
        angle_diff = abs(angle - target_angle)
        if angle_diff < best_angle_diff:
            best_angle_diff = angle_diff
            ref_beam_f3_point = test_point
            best_path_to_beam = path_points_to_beam
    
    if best_angle_diff > 10:  # If we couldn't get within 10 degrees of target
        print(f"Warning: Best angle found differs from target by {best_angle_diff:.1f} degrees")
    
    return ref_beam_f3_point, {
        "Nz-Vertex_Real": path_points_nz_vertex,
        "Ref-Beam": best_path_to_beam,
        "Ref_Beam_F3": ref_beam_f3_point
    }

