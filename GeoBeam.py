# Geodesic Beam F3 Localization Tool: GeoBeam

# A Python script for geodesic-based, post hoc reconstruction of individual Beam F3 TMS stimulation targets from structural MRI, as described in:

# Post Hoc Localization of Beam F3 Stimulation Targets: An MRI-Derived Geodesic Approach for Refined TMS E-Field Simulations
# Schramm S, Ten Pas J, Calabrò D, Jakubetz J, Szillat M, Koti J, Huang M, Kim SH, Woletz M, Kirschke J, Hedderich DM, Sollmann N, Tik M, Vogelmann U.

# Overview
# The Beam F3 method determines the TMS stimulation site over the left dorsolateral prefrontal cortex (dlPFC) based on individual cranial measurements. This script reconstructs subject-specific Beam F3 target coordinates geodesically from individual 3D scalp meshes generated by [SimNIBS](https://simnibs.github.io/simnibs/). It also determines a set of anatomically defined reference points (Cz_AP, Vertex_Real) and computes standard cranial distances (Nasion–Inion, Tragus–Tragus, circumference) needed for the Beam F3 formula.
# Key outputs include MNI-compatible 3D coordinates of the estimated stimulation site, a reference direction point for coil orientation (Ref_Beam_F3), and optional geodesic-based electrode positions for quality assurance.
# Importantly, at this stage you will need to run the functions to compute the basic anatomical distances (Nasion–Inion, Tragus–Tragus, circumference), input those into the external Beam F3 online tool (https://clinicalresearcher.org/F3/) to compute xbeam and ybeam, then import these distance into the GeoBeam script to find the target coordinates. We are working on integrating the formula from the online tool into the script to facilitate this process in future iterations. 
# Note: This is a research prototype released alongside the above preprint. It implements the core functionality described in the paper but has not been packaged as a general-purpose library. It has been tested on the dataset and software environment described in the paper. Use accordingly.

# Version 0.1 published with preprint June 2026. Contains core functions. Future iterations are under development. Primary focus at the moment will be to eliminate reliance on external online Beam F3 tool and compute X and Y within-script.


