def find_ref_point_on_nz_vertexreal(mesh, target_point, m2m_dir, geoalg=None, target_angle=45.0):
    """Generalized function to find the reference point on Nz-Vertex_Real path for a given target point."""
    # Get coordinates from EEG positions CSV
    eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
    eeg_df = pd.read_csv(eeg_path, header=None)
    label_col = eeg_df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    # Get Nz coordinates
    nz_match = eeg_df[eeg_df[label_col] == "Nz"]
    if nz_match.empty:
        raise ValueError("Nz not found in EEG positions CSV")
    nz_coords = nz_match.iloc[0, coord_cols].astype(float).values
    # Prepare mesh for geodesic calculations if needed
    if geoalg is None:
        points, faces = prepare_mesh_for_geodesic(mesh)
        geoalg = PyGeodesicAlgorithmExact(points, faces)
    else:
        points = mesh.points
    # Find Vertex_Real directly
    vertex_Real, _ = find_vertex_Real(mesh, m2m_dir, geoalg)
    # Find indices for all points
    nz_idx = np.argmin(np.linalg.norm(points - nz_coords, axis=1))
    vertex_real_idx = np.argmin(np.linalg.norm(points - vertex_Real, axis=1))
    target_idx = np.argmin(np.linalg.norm(points - target_point, axis=1))
    # Get Nz-Vertex_Real path
    _, path_nz_vertex = geoalg.geodesicDistance(nz_idx, vertex_real_idx)
    path_points_nz_vertex = np.array(path_nz_vertex)
    # Find the point where the intersection angle is closest to target
    best_angle_diff = float('inf')
    ref_point = None
    best_path_to_target = None
    num_samples = min(100, len(path_points_nz_vertex))
    sample_indices = np.linspace(0, len(path_points_nz_vertex)-1, num_samples, dtype=int)
    for idx in sample_indices:
        test_point = path_points_nz_vertex[idx]
        test_point_idx = np.argmin(np.linalg.norm(points - test_point, axis=1))
        # Calculate path from test point to target
        _, path_to_target = geoalg.geodesicDistance(test_point_idx, target_idx)
        path_points_to_target = np.array(path_to_target)
        # Calculate intersection angle
        angle = calculate_geodesic_angle(
            points,
            path_points_nz_vertex,
            path_points_to_target,
            idx
        )
        angle_diff = abs(angle - target_angle)
        if angle_diff < best_angle_diff:
            best_angle_diff = angle_diff
            ref_point = test_point
            best_path_to_target = path_points_to_target
    if best_angle_diff > 10:
        print(f"Warning: Best angle found for reference point differs from target by {best_angle_diff:.1f} degrees")
    return ref_point, {
        "Nz-Vertex_Real": path_points_nz_vertex,
        "Ref-Path": best_path_to_target,
        "Ref_Point": ref_point
    }


