def calculate_reference_distances(mesh, m2m_dir, Cz_AP=None, geoalg=None):
    """Calculate reference distances for Tr-Tr, Nz-Cz_AP, and Circ measurements.
    
    Args:
        mesh: PyVista mesh object
        m2m_dir: Directory containing measurements and data
        Cz_AP: Optional pre-calculated Cz_AP coordinates
        geoalg: Optional pre-initialized geodesic algorithm
    """
    # Create measurements directory if it doesn't exist
    measurements_dir = os.path.join(m2m_dir, "measurements")
    os.makedirs(measurements_dir, exist_ok=True)
    
    # Get coordinates from EEG positions CSV
    eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
    eeg_df = pd.read_csv(eeg_path, header=None)
    label_col = eeg_df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    
    # Get all required electrode coordinates
    electrodes = ["Nz", "Iz", "T7", "T8", "Fpz", "T7"]
    electrode_coords = {}
    electrode_indices = {}
    
    # Get EEG positions
    for electrode in electrodes:
        match = eeg_df[eeg_df[label_col] == electrode]
        if match.empty:
            raise ValueError(f"{electrode} not found in EEG positions CSV")
        coords = match.iloc[0, coord_cols].astype(float).values
        electrode_coords[electrode] = coords
    
    # Get fiducial coordinates (LPA, RPA)
    fiducials = ["LPA", "RPA"]
    for fiducial in fiducials:
        coords = get_fiducial_coords(m2m_dir, fiducial)
        electrode_coords[fiducial] = coords
    
    # Prepare mesh for geodesic calculations if geoalg is not provided
    if geoalg is None:
        points, faces = prepare_mesh_for_geodesic(mesh)
        geoalg = PyGeodesicAlgorithmExact(points, faces)
    else:
        # Get points from the mesh instead of geoalg
        points = np.array(mesh.points)
    
    # Find or use provided Cz_AP coordinates
    if Cz_AP is None:
        Cz_AP, _ = find_Cz_AP(mesh, m2m_dir, geoalg)
    electrode_coords["Cz_AP"] = Cz_AP
    
    # Find indices for all electrodes
    for name, coords in electrode_coords.items():
        electrode_indices[name] = np.argmin(np.linalg.norm(points - coords, axis=1))
    
    # Calculate Nz to Fpz path to find EB point
    nz_fpz_path = []
    fpz_match = eeg_df[eeg_df[label_col] == "Fpz"]
    if not fpz_match.empty:
        fpz_coords = fpz_match.iloc[0, coord_cols].astype(float).values
        fpz_idx = np.argmin(np.linalg.norm(points - fpz_coords, axis=1))
        distance_nz_fpz, path_nz_fpz = geoalg.geodesicDistance(
            electrode_indices["Nz"], 
            fpz_idx
        )
        path_points_nz_fpz = np.array(path_nz_fpz)
        
        # Calculate EB position
        total_distance = 0
        path_distances = []
        for i in range(1, len(path_points_nz_fpz)):
            segment_distance = np.linalg.norm(path_points_nz_fpz[i] - path_points_nz_fpz[i-1])
            total_distance += segment_distance
            path_distances.append(total_distance)
        
        target_distance = total_distance * 1/3
        eb_idx = np.argmin(np.abs(np.array(path_distances) - target_distance))
        eb_point = path_points_nz_fpz[eb_idx + 1]
        eb_idx_geo = np.argmin(np.linalg.norm(points - eb_point, axis=1))
        
        # Calculate Circuit distance (EB-T7-Iz-T8-EB)
        circ_paths = {
            "EB-T7": geoalg.geodesicDistance(eb_idx_geo, electrode_indices["T7"]),
            "T7-Iz": geoalg.geodesicDistance(electrode_indices["T7"], electrode_indices["Iz"]),
            "Iz-T8": geoalg.geodesicDistance(electrode_indices["Iz"], electrode_indices["T8"]),
            "T8-EB": geoalg.geodesicDistance(electrode_indices["T8"], eb_idx_geo)
        }
        circ_distance = sum(distance for distance, _ in circ_paths.values())
    else:
        raise ValueError("Fpz not found in EEG positions CSV")
    
    # Calculate Tr-Tr (LPA-Cz_AP + RPA-Cz_AP)
    lpa_Cz_AP_distance, _ = geoalg.geodesicDistance(electrode_indices["LPA"], electrode_indices["Cz_AP"])
    rpa_Cz_AP_distance, _ = geoalg.geodesicDistance(electrode_indices["RPA"], electrode_indices["Cz_AP"])
    tr_tr_distance = lpa_Cz_AP_distance + rpa_Cz_AP_distance
    
    # Calculate Nz-Cz_AP (Nz-Cz_AP + Iz-Cz_AP)
    nz_Cz_AP_path_distance, _ = geoalg.geodesicDistance(electrode_indices["Nz"], electrode_indices["Cz_AP"])
    iz_Cz_AP_path_distance, _ = geoalg.geodesicDistance(electrode_indices["Iz"], electrode_indices["Cz_AP"])
    nz_Cz_AP_iz_distance = nz_Cz_AP_path_distance + iz_Cz_AP_path_distance

    # Find or use provided Vertex_Real coordinates
    vertex_Real_coords, _ = find_vertex_Real(mesh, m2m_dir, geoalg, Cz_AP=Cz_AP)
    electrode_coords["Vertex_Real"] = vertex_Real_coords

    # Add Vertex_Real to electrode indices
    electrode_indices["Vertex_Real"] = np.argmin(np.linalg.norm(points - vertex_Real_coords, axis=1))

    # Calculate Vertex_Real distances
    vertex_real_lpa_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], electrode_indices["LPA"])
    vertex_real_rpa_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], electrode_indices["RPA"])
    vertex_real_iz_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], electrode_indices["Iz"])
    vertex_real_nz_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], electrode_indices["Nz"])
    vertex_real_cz_ap_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], electrode_indices["Cz_AP"])

    # For Cz distance, need to get original Cz coordinates
    cz_match = eeg_df[eeg_df[label_col] == "Cz"]
    if not cz_match.empty:
        cz_coords = cz_match.iloc[0, coord_cols].astype(float).values
        cz_idx = np.argmin(np.linalg.norm(points - cz_coords, axis=1))
        vertex_real_cz_distance, _ = geoalg.geodesicDistance(electrode_indices["Vertex_Real"], cz_idx)
    else:
        vertex_real_cz_distance = 0  # Or handle as appropriate

    
    # Print results
    print("\nReference Distance Measurements:")
    # Calculate R1 (Cz_AP to Fpz)
    r1_distance, _ = geoalg.geodesicDistance(electrode_indices["Cz_AP"], electrode_indices["Fpz"])
    
    # Calculate R2 (Cz_AP to T7)
    r2_distance, _ = geoalg.geodesicDistance(electrode_indices["Cz_AP"], electrode_indices["T7"])

    print(f"Tr-Tr (LPA-Cz_AP + RPA-Cz_AP): {tr_tr_distance:.2f} mm")
    print(f"  - LPA-Cz_AP: {lpa_Cz_AP_distance:.2f} mm")
    print(f"  - RPA-Cz_AP: {rpa_Cz_AP_distance:.2f} mm")
    print(f"Nz-Cz_AP-Iz (Nz-Cz_AP + Iz-Cz_AP): {nz_Cz_AP_iz_distance:.2f} mm")
    print(f"  - Nz-Cz_AP: {nz_Cz_AP_path_distance:.2f} mm")
    print(f"  - Iz-Cz_AP: {iz_Cz_AP_path_distance:.2f} mm")
    print(f"Circ (EB-T7-Iz-T8-EB): {circ_distance:.2f} mm")
    print(f"R1 (Cz_AP-Fpz): {r1_distance:.2f} mm")
    print(f"R2 (Cz_AP-T7): {r2_distance:.2f} mm")
    print(f"Vertex_Real-LPA: {vertex_real_lpa_distance:.2f} mm")
    print(f"Vertex_Real-RPA: {vertex_real_rpa_distance:.2f} mm")
    print(f"Vertex_Real-Iz: {vertex_real_iz_distance:.2f} mm")
    print(f"Vertex_Real-Nz: {vertex_real_nz_distance:.2f} mm")
    print(f"Vertex_Real-Cz: {vertex_real_cz_distance:.2f} mm")
    print(f"Vertex_Real-Cz_AP: {vertex_real_cz_ap_distance:.2f} mm")

    
    # Save results to CSV in measurements directory
    output_csv_path = os.path.join(measurements_dir, "reference_distances.csv")
    with open(output_csv_path, mode="w", newline="") as file:
        writer = csv.writer(file)
        writer.writerow(["Measurement", "Distance (mm)"])
        writer.writerow(["Tr-Tr", tr_tr_distance])
        writer.writerow(["LPA-Cz_AP", lpa_Cz_AP_distance])
        writer.writerow(["RPA-Cz_AP", rpa_Cz_AP_distance])
        writer.writerow(["Nz-Cz_AP-Iz", nz_Cz_AP_iz_distance])
        writer.writerow(["Nz-Cz_AP", nz_Cz_AP_path_distance])
        writer.writerow(["Iz-Cz_AP", iz_Cz_AP_path_distance])
        writer.writerow(["Circ", circ_distance])
        writer.writerow(["R1", r1_distance])
        writer.writerow(["R2", r2_distance])
        writer.writerow(["Vertex_Real-LPA", vertex_real_lpa_distance])
        writer.writerow(["Vertex_Real-RPA", vertex_real_rpa_distance])
        writer.writerow(["Vertex_Real-Iz", vertex_real_iz_distance])
        writer.writerow(["Vertex_Real-Nz", vertex_real_nz_distance])
        writer.writerow(["Vertex_Real-Cz", vertex_real_cz_distance])
        writer.writerow(["Vertex_Real-Cz_AP", vertex_real_cz_ap_distance])


    print(f"\nDistances saved to {output_csv_path}")
    
    # Calculate X using R1 and R2 values
    # This function is still under development. We are planning to calculate X and Y distances within-script to avoid the additional steps using the Beam F3 online tool.
    def calculate_X(r1, r2):
        """Calculate X using the provided formula with R1 and R2 values."""
        # Convert angles to radians for numpy trig functions
        angle_288 = np.radians(288)
        angle_324 = np.radians(324)
        
        # Break down the formula into parts for clarity
        numerator_part = r1 + ((r2 * r1 * np.sin(angle_288)) / (r1 * np.cos(angle_288) - r2))
        denominator_part = (r1 * np.sin(angle_288) / (r1 * np.cos(angle_288) - r2)) - \
                          ((r2 * np.sin(angle_324) + r1) / (r2 * np.cos(angle_324)))
        
        x = (1/2) * (numerator_part / denominator_part)
        return x
    
    x_value = calculate_X(r1_distance, r2_distance)
    print(f"\nCalculated X value: {x_value:.2f} mm")
    
    return {
        "tr_tr": tr_tr_distance,
        "nz_Cz_AP_iz": nz_Cz_AP_iz_distance,
        "circ": circ_distance,
        "r1": r1_distance,
        "r2": r2_distance,
        "x": x_value
    }

def find_vertex_Real(mesh, m2m_dir, geoalg=None, Cz_AP=None):
    """Find the Vertex_Real point using the Tr-Tr (LPA-RPA) distance through Cz_AP.
    
    Args:
        mesh: PyVista mesh object
        m2m_dir: Directory containing EEG positions
        geoalg: Optional pre-initialized geodesic algorithm
        Cz_AP: Optional pre-calculated Cz_AP coordinates
        
    Returns:
        tuple: (vertex_Real_coords, paths_dict) containing the coordinates of Vertex_Real
              and dictionary of paths for visualization
    """
    # Get fiducial coordinates for LPA and RPA
    lpa_coords = get_fiducial_coords(m2m_dir, "LPA")
    rpa_coords = get_fiducial_coords(m2m_dir, "RPA")

    # Use provided or calculate Cz_AP coordinates
    if Cz_AP is None:
        Cz_AP, _ = find_Cz_AP(mesh, m2m_dir, geoalg)

    # Prepare mesh for geodesic calculations if needed
    if geoalg is None:
        points, faces = prepare_mesh_for_geodesic(mesh)
        geoalg = PyGeodesicAlgorithmExact(points, faces)
    else:
        points = np.array(mesh.points)
        
    # Get indices for LPA, RPA, and Cz_AP
    lpa_idx = np.argmin(np.linalg.norm(points - lpa_coords, axis=1))
    rpa_idx = np.argmin(np.linalg.norm(points - rpa_coords, axis=1))
    Cz_AP_idx = np.argmin(np.linalg.norm(points - Cz_AP, axis=1))
    
    # Calculate paths from Cz_AP to LPA and RPA
    lpa_path_distance, lpa_path = geoalg.geodesicDistance(Cz_AP_idx, lpa_idx)
    rpa_path_distance, rpa_path = geoalg.geodesicDistance(Cz_AP_idx, rpa_idx)
    
    # Calculate total Tr-Tr distance
    tr_tr_distance = lpa_path_distance + rpa_path_distance
    
    # Convert paths to numpy arrays
    lpa_path_points = np.array(lpa_path)
    rpa_path_points = np.array(rpa_path)
    
    # Calculate cumulative distances along each path
    lpa_distances = np.zeros(len(lpa_path_points))
    rpa_distances = np.zeros(len(rpa_path_points))
    
    for i in range(1, len(lpa_path_points)):
        lpa_distances[i] = lpa_distances[i-1] + np.linalg.norm(lpa_path_points[i] - lpa_path_points[i-1])
    
    for i in range(1, len(rpa_path_points)):
        rpa_distances[i] = rpa_distances[i-1] + np.linalg.norm(rpa_path_points[i] - rpa_path_points[i-1])
    
    # Determine which path is longer and use it to find Vertex_Real
    if lpa_path_distance >= rpa_path_distance:
        print("\nLPA-Cz_AP path is longer, placing Vertex_Real on this path...")
        target_distance = tr_tr_distance / 2
        vertex_idx = np.argmin(np.abs(lpa_distances - target_distance))
        vertex_Real_coords = lpa_path_points[vertex_idx]
    else:
        print("\nCz_AP-RPA path is longer, placing Vertex_Real on this path...")
        target_distance = tr_tr_distance / 2
        vertex_idx = np.argmin(np.abs(rpa_distances - target_distance))
        vertex_Real_coords = rpa_path_points[vertex_idx]
    
    vertex_Real_idx = np.argmin(np.linalg.norm(points - vertex_Real_coords, axis=1))
    
    # Calculate the individual path segments from LPA to Vertex_Real and Vertex_Real to RPA
    lpa_vertex_distance, lpa_vertex_path = geoalg.geodesicDistance(lpa_idx, vertex_Real_idx)
    vertex_rpa_distance, vertex_rpa_path = geoalg.geodesicDistance(vertex_Real_idx, rpa_idx)
    
    # Create comprehensive paths dictionary for visualization
    paths_dict = {
        "LPA-Cz_AP": lpa_path_points,
        "RPA-Cz_AP": rpa_path_points,
        "LPA-Vertex": np.array(lpa_vertex_path),
        "Vertex-RPA": np.array(vertex_rpa_path),
        "Vertex_Real": vertex_Real_coords,
        "LPA": lpa_coords,
        "RPA": rpa_coords
    }
    
    return vertex_Real_coords, paths_dict


def main(mesh_file=None, m2m_dir=None, eeg_position="Cz", decimation_factor=0.9, 
         visualize_fiducials=False, visualize_plane=False, visualize_perpendicular=False, 
         visualize_nz_fpz=False, visualize_center=False, calculate_distances=True,
         calculate_beam_f3=False, x_f3_geo=None, y_f3_geo=None, visualize_custom_coords=None,
         calculate_Cz_AP=False, calculate_vertex_Real=True, determine_geo_electrodes=False, geo_off_screen=True,
         x_f3_irl=None, y_f3_irl=None):
    """Main function to process mesh and calculate points of interest."""
    # Verify required parameters
    if mesh_file is None or m2m_dir is None:
        raise ValueError("mesh_file and m2m_dir are required parameters")

    # Create output directory for results
    output_dir = os.path.join(m2m_dir, "measurements")
    os.makedirs(output_dir, exist_ok=True)

    # Load and decimate mesh if needed
    print(f"\nLoading mesh from {mesh_file}...")
    print("Extracting scalp mesh...")
    scalp_mesh = extract_scalp_from_simnibs(mesh_file, decimation_factor=decimation_factor)
    print("Cleaning and repairing mesh...")
    repaired_mesh = clean_and_repair_mesh(scalp_mesh)

    # Initialize geodesic algorithm once to reuse
    points, faces = prepare_mesh_for_geodesic(repaired_mesh)
    geoalg = PyGeodesicAlgorithmExact(points, faces)

    # Calculate Cz_AP first since it's needed for other calculations
    print("\nCalculating Cz_AP position...")
    Cz_AP_coords, Cz_AP_paths_dict = find_Cz_AP(repaired_mesh, m2m_dir, geoalg)
    print(f"Cz_AP coordinates: [{Cz_AP_coords[0]:.2f}, {Cz_AP_coords[1]:.2f}, {Cz_AP_coords[2]:.2f}]")

    # Calculate reference distances using pre-calculated Cz_AP
    if calculate_distances:
        calculate_reference_distances(repaired_mesh, m2m_dir, Cz_AP=Cz_AP_coords, geoalg=geoalg)

    # Calculate Vertex_Real if requested, using pre-calculated Cz_AP
    vertex_Real_paths_dict = None
    if calculate_vertex_Real:
        print("\nCalculating Vertex_Real position...")
        vertex_Real_coords, vertex_Real_paths_dict = find_vertex_Real(
            repaired_mesh, 
            m2m_dir,
            geoalg=geoalg,
            Cz_AP=Cz_AP_coords
        )
        print(f"Vertex_Real coordinates: [{vertex_Real_coords[0]:.2f}, {vertex_Real_coords[1]:.2f}, {vertex_Real_coords[2]:.2f}]")

        # Export Vertex_Real data
        coords_output = os.path.join(output_dir, "vertex_real_coordinates.csv")
        distances_output = os.path.join(output_dir, "vertex_real_distances.csv")

        # Prepare coordinates dictionary
        coords_dict = {
            "Vertex_Real": vertex_Real_coords,
            "Cz_AP": Cz_AP_coords,
            "LPA": vertex_Real_paths_dict["LPA"],
            "RPA": vertex_Real_paths_dict["RPA"]
        }
        export_point_coordinates(coords_dict, coords_output)

        # Calculate and export distances
        distances_dict = {
            "LPA_to_Vertex_Real": np.sum([np.linalg.norm(vertex_Real_paths_dict["LPA-Vertex"][i] - 
                                        vertex_Real_paths_dict["LPA-Vertex"][i-1]) 
                                        for i in range(1, len(vertex_Real_paths_dict["LPA-Vertex"]))]),
            "Vertex_Real_to_RPA": np.sum([np.linalg.norm(vertex_Real_paths_dict["Vertex-RPA"][i] - 
                                        vertex_Real_paths_dict["Vertex-RPA"][i-1]) 
                                        for i in range(1, len(vertex_Real_paths_dict["Vertex-RPA"]))])
        }
        export_distances(distances_dict, distances_output)

    # Export Cz_AP data if requested
    if calculate_Cz_AP:
        coords_output = os.path.join(output_dir, "Cz_AP_coordinates.csv")
        distances_output = os.path.join(output_dir, "Cz_AP_distances.csv")
        export_point_coordinates({"Cz_AP": Cz_AP_coords}, coords_output)

    # Calculate beam F3 coordinates if requested
    if calculate_beam_f3:
        if x_f3_geo is None or y_f3_geo is None:
            raise ValueError("x_f3_geo and y_f3_geo are required for beam F3 calculation")
        f3_coords, ref_beam_f3_geo_coords, f3_paths_dict = calculate_beam_f3_coordinates(repaired_mesh, m2m_dir, x_f3_geo, y_f3_geo)
        print(f"\nF3 coordinates: [{f3_coords[0]:.2f}, {f3_coords[1]:.2f}, {f3_coords[2]:.2f}]")

        # Export Beam_F3 and Ref_Beam_F3 (geo) coordinates
        coords_output = os.path.join(output_dir, "beam_f3_coordinates.csv")
        export_point_coordinates({"Beam_F3": f3_coords, "Ref_Beam_F3": ref_beam_f3_geo_coords}, coords_output)

        # Export F3 coordinates (legacy, for compatibility)
        coords_output_f3 = os.path.join(output_dir, "f3_coordinates.csv")
        export_point_coordinates({"F3": f3_coords}, coords_output_f3)

        # Visualize F3 position
        visualize_beam_f3(repaired_mesh, f3_paths_dict)
        
        # --- Beam_F3_irl calculation ---
        beam_f3_irl_coords = None
        ref_beam_f3_irl_coords = None
        if x_f3_irl is not None and y_f3_irl is not None:
            beam_f3_irl_coords, ref_beam_f3_irl_coords, beam_f3_irl_paths_dict = calculate_beam_f3_coordinates(repaired_mesh, m2m_dir, x_f3_irl, y_f3_irl)
            print(f"\nBeam_F3_irl coordinates: [{beam_f3_irl_coords[0]:.2f}, {beam_f3_irl_coords[1]:.2f}, {beam_f3_irl_coords[2]:.2f}]")
            # Export Beam_F3_irl and Ref_Beam_F3_irl coordinates
            coords_output_irl = os.path.join(output_dir, "beam_f3_irl_coordinates.csv")
            export_point_coordinates({"Beam_F3_irl": beam_f3_irl_coords, "Ref_Beam_F3_irl": ref_beam_f3_irl_coords}, coords_output_irl)

    # Other visualization options
    if visualize_nz_fpz:
        visualize_nz_fpz_path(repaired_mesh, m2m_dir)
        
    if visualize_center:
        visualize_center_paths(repaired_mesh, m2m_dir)
        
    if visualize_plane:
        visualize_mesh_plane_intersection(repaired_mesh, Cz_AP_paths_dict)
        
    if visualize_perpendicular:
        lpa = get_fiducial_coords(m2m_dir, "LPA")
        rpa = get_fiducial_coords(m2m_dir, "RPA")
        visualize_perpendicular_plane(repaired_mesh, lpa, rpa, Cz_AP_coords)

    if visualize_fiducials:
        # Get fiducial coordinates for Iz and Nz
        iz_coords = get_fiducial_coords(m2m_dir, "Iz")
        nz_coords = get_fiducial_coords(m2m_dir, "Nz")

        # Get source point
        source_coords = iz_coords if eeg_position == "Iz" else nz_coords
        source_idx = np.argmin(np.linalg.norm(points - source_coords, axis=1))
        distances, _ = compute_geodesic_distances(repaired_mesh, source_point_index=source_idx)
        visualize_geodesic(repaired_mesh, distances, source_idx)

    # Show combined visualization of Cz_AP and Vertex_Real
    if calculate_vertex_Real:
        visualize_cz_and_vertex_Real(repaired_mesh, Cz_AP_paths_dict, vertex_Real_paths_dict, m2m_dir)

    # --- GEO ELECTRODES ---
    if determine_geo_electrodes:
        print("\n[DEBUG] Starting determine_electrodes_geo...")
        print(f"[DEBUG] Output directory: {output_dir}")
        determine_electrodes_geo(repaired_mesh, m2m_dir, geoalg=geoalg, output_dir=output_dir, off_screen=geo_off_screen)
        print("[DEBUG] Finished determine_electrodes_geo.")

    # --- F3, F3_Geo, Beam_F3 comparison visualization ---
    if determine_geo_electrodes and calculate_beam_f3:
        # Load F3 (CSV)
        F3_csv = None
        eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
        if os.path.exists(eeg_path):
            eeg_df = pd.read_csv(eeg_path, header=None)
            label_col = eeg_df.shape[1] - 1
            coord_cols = [label_col - 3, label_col - 2, label_col - 1]
            f3_match = eeg_df[eeg_df[label_col] == "F3"]
            if not f3_match.empty:
                F3_csv = f3_match.iloc[0, coord_cols].astype(float).values
