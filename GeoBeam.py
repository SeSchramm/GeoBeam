def poisson_reconstruction_fallback(mesh):
    """Poisson surface reconstruction for severely damaged meshes using Open3D."""
    import open3d as o3d
    pcd = o3d.geometry.PointCloud()
    pcd.points = o3d.utility.Vector3dVector(mesh.points)
    pcd.normals = o3d.utility.Vector3dVector(mesh.point_normals)
    mesh_o3d, _ = o3d.geometry.TriangleMesh.create_from_point_cloud_poisson(
        pcd, depth=9, width=0, scale=1.1, linear_fit=False
    )
    vertices = np.asarray(mesh_o3d.vertices)
    faces = np.asarray(mesh_o3d.triangles)
    faces_pv = np.column_stack([np.full(len(faces), 3), faces]).flatten()
    return pv.PolyData(vertices, faces_pv)

def extract_scalp_from_simnibs(msh_file, decimation_factor=0.9):  # The decimation factor is used to simplify the mesh for faster processing. A decimation factor of 0.9 would eliminate 90% of the vertices and simplify the mesh accordingly. Use to speed up iterations when trying things out.
    """Extract scalp surface from a SimNIBS mesh file."""
    print(f"Loading mesh from {msh_file}...")
    try:
        mesh = pv.read(msh_file)
    except Exception:
        try:
            temp_vtk = "temp_mesh.vtk"
            mesh_io = meshio.read(msh_file)
            meshio.write(temp_vtk, mesh_io)
            mesh = pv.read(temp_vtk)
            os.remove(temp_vtk)
        except Exception as e:
            raise ValueError(f"Failed to read mesh file: {e}")
    print("Extracting scalp surface...")
    if hasattr(mesh, 'cell_data') and 'gmsh:physical' in mesh.cell_data:
        skin_cells = mesh.cell_data['gmsh:physical'] == 5    # This is the default value for the scalp surface in SimNIBS.
        if np.any(skin_cells):
            scalp_mesh = mesh.extract_cells(skin_cells)
        else:
            print("No scalp cells found with gmsh:physical=1, extracting outer surface...")
            scalp_mesh = mesh.extract_surface()
    else:
        print("No gmsh:physical tags found, extracting outer surface...")
        scalp_mesh = mesh.extract_surface()
    scalp_mesh = scalp_mesh.extract_surface()
    print(f"Decimating mesh by {decimation_factor*100:.0f}%...")
    return scalp_mesh.decimate(decimation_factor)

def validate_faces(points, faces):
    """Validate face indices and topology."""
    if faces.min() < 0 or faces.max() >= points.shape[0]:
        raise ValueError(f"Invalid face indices: min={faces.min()}, max={faces.max()}")
    valid_faces = [f for f in faces if len(np.unique(f)) == 3]
    return np.array(valid_faces, dtype=np.int32)

def prepare_mesh_for_geodesic(mesh):
    """Prepare a PyVista mesh for geodesic computations."""
    points = np.array(mesh.points, dtype=np.float64)
    faces = mesh.faces
    if faces.ndim == 1 and faces.size % 4 == 0 and np.all(faces[::4] == 3):
        faces = faces.reshape(-1, 4)[:, 1:]
    elif faces.ndim == 2 and faces.shape[1] == 3:
        pass
    else:
        raise ValueError(f"Unexpected faces array shape: {faces.shape}")
    faces = faces.astype(np.int32)
    faces = validate_faces(points, faces)
    if faces.size == 0:
        raise ValueError("No faces found in the mesh after preprocessing.")
    if not np.all(np.isfinite(points)):
        raise ValueError("Non-finite values found in points after normalization.")
    print(f"Mesh prepared: {points.shape[0]} points, {faces.shape[0]} triangles")
    return points, faces

def compute_geodesic_distances(mesh, source_point_index=None, m2m_dir=None, eeg_position=None):
    """Compute geodesic distances on the mesh from a specified point or EEG position."""
    points, faces = prepare_mesh_for_geodesic(mesh)
    if eeg_position and m2m_dir:
        eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
        if os.path.exists(eeg_path):
            eeg_df = pd.read_csv(eeg_path, header=None)
            # The label is in the last column, coordinates are the three before
            label_col = eeg_df.shape[1] - 1
            coord_cols = [label_col - 3, label_col - 2, label_col - 1]
            match = eeg_df[eeg_df[label_col] == eeg_position]
            if not match.empty:
                coords = match.iloc[0, coord_cols].astype(float).values
                source_point_index = np.argmin(np.linalg.norm(mesh.points - coords, axis=1))
                print(f"Found {eeg_position} at coordinates {coords}")
    if source_point_index is None:
        print("Using highest point (Z axis) as source...")
        source_point_index = np.argmax(mesh.points[:, 2])
    print("Initializing PyGeodesicAlgorithmExact...")
    geoalg = PyGeodesicAlgorithmExact(points, faces)
    print(f"Computing geodesic distances from point {source_point_index}...")
    distances = geoalg.geodesicDistances([source_point_index])[0]
    finite_mask = np.isfinite(distances)
    if np.any(~finite_mask):
        print(f"Warning: {np.sum(~finite_mask)} points have infinite distances. Replacing with max+10%.")
        distances[~finite_mask] = distances[finite_mask].max() * 1.1 if np.any(finite_mask) else 1.0
    return distances, source_point_index

def visualize_geodesic(mesh, distances, source_point_index, n_contours=10):
    """Visualize the mesh with geodesic distances and contours."""
    print("Visualizing geodesic distances...")
    mesh["distances"] = distances
    max_dist = np.max(distances)
    contour_values = np.linspace(max_dist/n_contours, max_dist*(n_contours-1)/n_contours, n_contours-1)
    try:
        contours = mesh.contour(contour_values, scalars="distances")
    except Exception as e:
        print(f"Error creating contours: {e}")
        contours = None
    plotter = pv.Plotter()
    plotter.add_mesh(mesh, scalars="distances", cmap="viridis", scalar_bar_args={"title": "Geodesic Distance"})
    if contours is not None:
        plotter.add_mesh(contours, color="black", line_width=2)
    plotter.add_points(mesh.points[source_point_index:source_point_index+1], color="red", point_size=20, render_points_as_spheres=True)
    plotter.add_point_labels([mesh.points[source_point_index]], ["Source"], font_size=24, point_color="red", text_color="black")
    plotter.show()

def clean_and_repair_mesh(mesh):
    """Clean and repair mesh using PyMeshFix, fallback to Poisson if needed."""
    meshfix = mf.MeshFix(mesh)
    holes = meshfix.extract_holes()
    print(f"Found {holes.n_points} hole boundary points")
    meshfix.repair(verbose=True, joincomp=True, remove_smallest_components=True)
    repaired_mesh = meshfix.mesh
    if not repaired_mesh.is_manifold:
        print("Mesh is not manifold, applying Poisson reconstruction fallback...")
        repaired_mesh = poisson_reconstruction_fallback(repaired_mesh)
    return repaired_mesh

def get_fiducial_coords(m2m_dir, label):
    """Extracts the coordinates for a given fiducial label from Fiducials.csv in the m2m_dir/eeg_positions folder."""
    fiducials_path = os.path.join(m2m_dir, "eeg_positions/Fiducials.csv")
    if not os.path.exists(fiducials_path):
        raise FileNotFoundError(f"Fiducials.csv not found at {fiducials_path}")
    df = pd.read_csv(fiducials_path, header=None)
    label_col = df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    match = df[df[label_col] == label]
    if match.empty:
        raise ValueError(f"Fiducial '{label}' not found in {fiducials_path}")
    coords = match.iloc[0, coord_cols].astype(float).values
    return coords

def visualize_mesh_plane_intersection(mesh, paths_dict):
    """Visualize the intersection of the mesh with the plane defined by Iz, Nz, and Cz_AP.
    Also shows the complete Nz-Iz path with Cz_AP at the midpoint.
    
    Args:
        mesh: PyVista mesh object
        paths_dict: Dictionary containing paths and point coordinates from find_Cz_AP
    """
    # Extract points from paths_dict
    iz = paths_dict["Iz"]
    nz = paths_dict["Nz"]
    Cz_AP = paths_dict["Cz_AP"]
    
    # Compute the normal vector of the plane
    v1 = nz - iz
    v2 = Cz_AP - iz
    normal = np.cross(v1, v2)
    normal = normal / np.linalg.norm(normal)
    
    # The plane passes through Iz
    slice_result = mesh.slice(origin=iz, normal=normal)
    
    # Set up the visualization
    plotter = pv.Plotter()
    plotter.add_mesh(mesh, color='lightgray', opacity=0.3)
    
    # Add the plane intersection
    plotter.add_mesh(slice_result, color='red', line_width=3, label='Sagittal Plane')
    
    # Add the complete Nz-Iz path
    if "Nz-Iz_Path" in paths_dict:
        path_points = paths_dict["Nz-Iz_Path"]
        n_points = len(path_points)
        cells = np.full((n_points - 1, 3), 2, dtype=np.int_)
        cells[:, 1] = np.arange(0, n_points - 1)
        cells[:, 2] = np.arange(1, n_points)
        cells = cells.ravel()
        
        poly_line = pv.PolyData(path_points, lines=cells)
        plotter.add_mesh(poly_line, color='blue', line_width=5, label='Nz-Iz Path')
    
    # Add marker points with labels
    # Calculate sphere size based on mesh scale
    bbox = np.array(mesh.bounds)
    sphere_size = np.linalg.norm(bbox[1::2] - bbox[::2]) * 0.005
    
    point_colors = {
        "Iz": "red",
        "Nz": "blue",
        "Cz_AP": "yellow"
    }
    
    # Add points and labels
    for point_name, color in point_colors.items():
        if point_name in paths_dict:
            coords = paths_dict[point_name]
            sphere = pv.Sphere(radius=sphere_size*(1.5 if point_name == "Cz_AP" else 1.0),
                             center=coords)
            plotter.add_mesh(sphere, color=color, label=point_name)
            
            # Add coordinate labels for Cz_AP
            if point_name == "Cz_AP":
                plotter.add_point_labels(
                    [coords],
                    [f"{point_name}\n[{coords[0]:.1f}, {coords[1]:.1f}, {coords[2]:.1f}]"],
                    font_size=12,
                    text_color='black'
                )
    
    plotter.add_legend()
    plotter.show()

def visualize_perpendicular_plane(mesh, lpa, rpa, Cz_AP):
    """Visualize the intersection of the mesh with the plane defined by LPA-Cz_AP-RPA paths."""
    # Prepare mesh for geodesic calculations
    points, faces = prepare_mesh_for_geodesic(mesh)
    geoalg = PyGeodesicAlgorithmExact(points, faces)
    
    # Find indices for points
    lpa_idx = np.argmin(np.linalg.norm(points - lpa, axis=1))
    rpa_idx = np.argmin(np.linalg.norm(points - Cz_AP, axis=1))
    Cz_AP_idx = np.argmin(np.linalg.norm(points - rpa, axis=1))
    
    # Get paths through Cz_AP
    _, path_lpa_cz = geoalg.geodesicDistance(lpa_idx, Cz_AP_idx)
    _, path_cz_rpa = geoalg.geodesicDistance(Cz_AP_idx, rpa_idx)
    
    # Compute the normal vector of the plane using the paths through Cz_AP
    v1 = Cz_AP - lpa
    v2 = rpa - Cz_AP
    normal = np.cross(v1, v2)
    normal = normal / np.linalg.norm(normal)
    origin = lpa
    # Use PyVista to slice the mesh
    slice_result = mesh.slice(origin=origin, normal=normal)
    plotter = pv.Plotter()
    plotter.add_mesh(mesh, color='lightgray', opacity=0.3)
    plotter.add_mesh(slice_result, color='purple', line_width=5, label='LPA-RPA-Cz_AP Plane')
    # Add points with unique colors
    plotter.add_points(lpa[None, :], color='orange', point_size=20, render_points_as_spheres=True)
    plotter.add_points(rpa[None, :], color='cyan', point_size=20, render_points_as_spheres=True)
    plotter.add_points(Cz_AP[None, :], color='red', point_size=20, render_points_as_spheres=True)
    plotter.add_point_labels(np.vstack([lpa, rpa, Cz_AP]), ['LPA', 'RPA', 'Cz_AP'], 
                           font_size=16, point_color='black', text_color='black')
    plotter.add_legend()
    plotter.show()

def visualize_nz_fpz_path(mesh, m2m_dir):
    """Visualize the geodesic paths between multiple points on the scalp."""
    # Get coordinates from EEG positions CSV
    eeg_path = os.path.join(m2m_dir, "eeg_positions/EEG10-10_UI_Jurak_2007.csv")
    eeg_df = pd.read_csv(eeg_path, header=None)
    label_col = eeg_df.shape[1] - 1
    coord_cols = [label_col - 3, label_col - 2, label_col - 1]
    
    # Get all required electrode coordinates
    electrodes = ["Nz", "Fpz", "T7", "Iz", "T8"]
    electrode_coords = {}
    electrode_indices = {}
    
    for electrode in electrodes:
        match = eeg_df[eeg_df[label_col] == electrode]
        if match.empty:
            raise ValueError(f"{electrode} not found in EEG positions CSV")
        coords = match.iloc[0, coord_cols].astype(float).values
        electrode_coords[electrode] = coords
        print(f"{electrode} coordinates from CSV: {coords}")
    
    # Prepare mesh for geodesic calculations
    points, faces = prepare_mesh_for_geodesic(mesh)
    geoalg = PyGeodesicAlgorithmExact(points, faces)
    
    # Find indices for all electrodes in the geodesic mesh
    for electrode, coords in electrode_coords.items():
        idx = np.argmin(np.linalg.norm(points - coords, axis=1))
        electrode_indices[electrode] = idx
        print(f"{electrode} index in geodesic mesh: {idx}")
    
    # First find EB point (1/3 along Nz-Fpz path)
    distance_nz_fpz, path_nz_fpz = geoalg.geodesicDistance(
        electrode_indices["Nz"], 
        electrode_indices["Fpz"]
    )
    path_points_nz_fpz = np.array(path_nz_fpz)
    
    # Calculate EB position
    total_distance = 0
    path_distances = []
    for i in range(1, len(path_points_nz_fpz)):
        segment_distance = np.linalg.norm(path_points_nz_fpz[i] - path_points_nz_fpz[i-1])
        total_distance += segment_distance
        path_distances.append(total_distance)
    
    target_distance = total_distance * 1/3
    eb_idx = np.argmin(np.abs(np.array(path_distances) - target_distance))
    eb_point = path_points_nz_fpz[eb_idx + 1]
    
    # Find EB index in the geodesic mesh
    eb_idx_geo = np.argmin(np.linalg.norm(points - eb_point, axis=1))
    
    # Calculate path from EB to T7
    distance_eb_t7, path_eb_t7 = geoalg.geodesicDistance(
        eb_idx_geo,
        electrode_indices["T7"]
    )
    path_points_eb_t7 = np.array(path_eb_t7)
    
    # Calculate path from EB to T8
    distance_eb_t8, path_eb_t8 = geoalg.geodesicDistance(
        eb_idx_geo,
        electrode_indices["T8"]
    )
    path_points_eb_t8 = np.array(path_eb_t8)
    
    # Calculate all path segments
    paths = {
        "EB-T7": geoalg.geodesicDistance(eb_idx_geo, electrode_indices["T7"]),
        "T7-Iz": geoalg.geodesicDistance(electrode_indices["T7"], electrode_indices["Iz"]),
        "Iz-T8": geoalg.geodesicDistance(electrode_indices["Iz"], electrode_indices["T8"]),
        "T8-EB": geoalg.geodesicDistance(electrode_indices["T8"], eb_idx_geo)
    }
    
    # Store paths and calculate total distance
    total_circuit_distance = 0
    path_points = {}
    
    for name, (distance, path) in paths.items():
        path_points[name] = np.array(path)
        total_circuit_distance += distance
        print(f"Distance along {name}: {distance:.2f} mm")
    
    print(f"Total circuit distance: {total_circuit_distance:.2f} mm")
    
    # Visualize
    plotter = pv.Plotter()
    plotter.add_mesh(mesh, color='lightgray', opacity=0.3)
    
    # Colors for different paths
    path_colors = {
        "EB-T7": "magenta",
        "T7-Iz": "yellow",
        "Iz-T8": "cyan",
        "T8-EB": "red"
    }
    
    # Add all path segments
    for name, path_coords in path_points.items():
        n_points = len(path_coords)
        cells = np.full((n_points - 1, 3), 2, dtype=np.int_)
        cells[:, 1] = np.arange(0, n_points - 1)
        cells[:, 2] = np.arange(1, n_points)
        cells = cells.ravel()
        
        poly_line = pv.PolyData(path_coords, lines=cells)
        plotter.add_mesh(poly_line, color=path_colors[name], line_width=5, label=name)
    
    # Add spheres at all points
    sphere_size = np.linalg.norm(electrode_coords["Nz"] - electrode_coords["Fpz"]) * 0.02
    point_colors = {
        "Nz": "blue",
        "Fpz": "green",
        "T7": "orange",
        "Iz": "purple",
        "T8": "pink",
        "EB": "red"
    }
    
    # Add electrode points
    for electrode, coords in electrode_coords.items():
        sphere = pv.Sphere(radius=sphere_size, center=points[electrode_indices[electrode]])
        plotter.add_mesh(sphere, color=point_colors[electrode], label=electrode)
    
    # Add EB point (slightly larger)
    eb_sphere = pv.Sphere(radius=sphere_size*1.5, center=eb_point)
    plotter.add_mesh(eb_sphere, color=point_colors["EB"], label="EB")
    
    # Add labels
    plotter.add_legend()
    plotter.show()

